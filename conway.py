def print_and_wait(text):
    print( chr(27)+"[2J" + text)
    sys.stdout.flush()
    time.sleep(1)

def main():
    raw_universe = pattern_toad()
    game = Conway.GameOfLife.Game(raw_universe)
    print_and_wait(game.show_universe(''))
    for i in range(50):
        game.next_step()
        print_and_wait(game.show_universe(''))

# Standard boilerplate to call the main() function.
